    s0 = Td0[t0 >> 24] ^ Td1[(t3 >> 16) & 0xFF] ^ Td2[(t2 >>  8) & 0xFF] ^ Td3[t1 & 0xFF] ^ rk[16];
    s1 = Td0[t1 >> 24] ^ Td1[(t0 >> 16) & 0xFF] ^ Td2[(t3 >>  8) & 0xFF] ^ Td3[t2 & 0xFF] ^ rk[17];
